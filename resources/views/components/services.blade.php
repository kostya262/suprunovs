                    <div class="accordion-body">
                        <ul>
                            <li><b>Лечение</b>
                                <br>
                                Лечение зубов – это комплекс специальных мероприятий, позволяющих остановить развитие
                                воспалительного процесса, восстановить все полезные функции, улучшить эстетику
                                стоматологической установки.. Пациенты чаще всего обращаются к стоматологу для лечения
                                кариеса зубов, встречающегося в любом возрасте и в разных формах - от стадии пятна до
                                глубокого кариеса, когда разрушение единицы практически достигло корня.. Если кариес не
                                был вылечен вовремя, либо лечение проводилось с нарушением правил, возникают такие
                                осложнения, как пульпит и периодонтит.
                            </li>
                            <li><b>Лечение молочный и постоянных зубов</b>
                                <br>
                                Лечение молочных зубов у детей так же необходимо, как и лечение других хронических
                                заболеваний. Родители избегают похода к стоматологу, оправдываясь тем, что "это детский
                                зуб, он скоро выпадет". Это происходит потому, что они неправильно понимают природу
                                кариеса. Кариес вызывается бактериями, и постоянные зубы наследуют кариозные бактерии,
                                когда вырастают. Только лечение у детского стоматолога может остановить этот
                                разрушительный процесс.
                            </li>
                            <li><b>Седация</b>
                                <br>
                                Седация - физическое состояние, характеризующееся потерей или снижением сознания,
                                подавлением рефлексов и гипералгезией, искусственно вызываемое приемом седативных
                                препаратов. Его можно разделить на краткосрочное и долгосрочное успокоение.
                            </li>
                        </ul>
