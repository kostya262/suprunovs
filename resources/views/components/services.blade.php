                <div id="collapseTwo" class="accordion-collapse collapse" data-bs-parent="#accordionExample">
                    <div class="accordion-body">
                        <ul>
                            <li><b>Удаление любой сложности</b>
                                <br>
                                Удаление любой сложности - это радикальная мера удаления зуба, которая проводится в
                                наших клиниках только в крайних случаях, когда уже нет никакой возможности спасти зуб.
                                Чтобы избежать этой процедуры, необходимо вовремя обратиться к специалисту и не
                                допустить разрушения зуба. Но даже если вам придется удалить зуб, не стоит беспокоиться.
                                Сегодня удаление зуба - это безболезненная процедура, и последующие функциональные и
                                эстетические проблемы легко решаются.
                            </li>
                            <li><b>Коррекция уздечек</b>
                                <br>
                                Коррекция уздечек - это название складок, соединяющих верхнюю губу с челюстью. Он
                                участвует в открывании и закрывании рта, движении верхней губы и артикуляции. От
                                качества и эластичности крепления зависит подвижность полости рта, здоровье зубов,
                                правильный прикус, артикуляция и эстетика улыбки.
                            </li>
                            <li><b>Имплантология</b>
                                <br>
                                Имплантация - это хирургическая процедура, при которой в кость вживляется искусственный
                                корень зуба и на него крепится протез или коронка. Имплантат похож на винт (стержень),
                                который вживляется в кость и действует как корень зуба. Диаметр имплантата составляет
                                3-5 мм, а наиболее распространенными материалами являются титан и оксид циркония.
                            </li>
                            <li><b>Синус-лифтинг</b>
                                <br>
                                Синус-лифтинг - это процедура наращивания кости в верхней челюсти, когда ее недостаточно
                                для установки имплантата. Эта процедура недавно появилась в стоматологии, но уже
                                показала отличные результаты. Большинство пациентов хорошо переносят операцию, и через
                                несколько месяцев можно имплантировать титановый стержень и установить на него коронку
                            </li>
                            <li><b>НКР (направленная костная регенерация)</b>
                                <br>
                                НКР (направленная костная регенерация) - прямая регенерация кости - это процедура, при
                                которой недостаток костной ткани замещается заменителем, зафиксированным барьерной
                                мембраной. После операции вокруг трансплантата формируется скелет (образованный
                                кровеносными сосудами и костными клетками, которые создают новую ткань).
                            </li>
                        </ul>
