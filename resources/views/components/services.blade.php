                <h2 class="accordion-header">
                    <button class="accordion-button collapsed" type="button" data-bs-toggle="collapse"
                            data-bs-target="#collapseOne" aria-expanded="false" aria-controls="collapseOne">
                        Лечение зубов
                    </button>
                </h2>
                <div id="collapseOne" class="accordion-collapse collapse" data-bs-parent="#accordionExample">
                    <div class="accordion-body">
                        <ul>
                            <li><b>Кариес</b>
                                <br>
                                Кариес зубов-это типичный патологический процесс, который развивался в ходе эволюции как
                                реакция твердых тканей зуба на различные воздействия и постепенно привел к образованию
                                кариозных дефектов зубов.
                            </li>
                            <li><b>Пульпит</b>
                                <br>
                                Пульпит-это воспаление мягких тканей(кровеносных сосудов и нервных волокон) в зубе.Еще
                                один неприятный факт: пульпит во вставном зубе прогрессирует быстро.Эмаль такого зуба
                                тонкая, и кариес может молниеносно превратиться в воспаление пульпы.Приходите к нам за
                                помощью как можно скорее!
                            </li>
                            <li><b>Периодонтит</b>
                                <br>
                                Периодонтит - это воспаление корня зуба и окружающих его тканей. Чаще всего он
                                вызывается нелеченым пульпитом. При периодонтите разрушаются нервы и поражаются связки,
                                поддерживающие зубы. Поэтому малейшее прикосновение вызывает боль.
                            </li>
                            <li><b>Эстетические реставрации</b>
                                <br>
                                Эстетические реставрации - комплекс стоматологических процедур, направленных на
                                улучшение эстетики зубов, восстановление их внешнего вида и максимальное приближение к
                                физиологическому состоянию.
                            </li>
                            <li><b>Шинирование зубов</b>
                                <br>
                                Шинирование зубов- шины можно разделить на временные и постоянные, в зависимости от
                                длительности использования. Временные шины используются на срок до года при острых
                                воспалительных заболеваниях или при подготовке к операции. Постоянные шины в основном
                                представляют собой фиксированные опоры и используются в случаях атрофии костной ткани
                                или хронических заболеваний (например, пародонтита).
                            </li>
                            <li><b>Реминерализирующая терапия</b>
                                <br>
                                Реминерализующая терапия - это комбинированное лечебно-восстановительное мероприятие,
                                необходимое для нормализации минерального состава зубной эмали. Она может применяться
                                для устранения микроскопических дефектов эмали и для ее укрепления.
                            </li>
                            <li><b>Гигиена</b>
                                <br>
                                Профессиональная гигиена полости рта (чистка зубов) - это система
                                лечебно-профилактических мероприятий, проводимых в стоматологических клиниках,
                                направленная на предотвращение развития и прогрессирования заболеваний полости рта. Врач
                                или гигиенист удаляет налет и зубной камень и полирует поверхность зубов.
                                Профессиональная гигиена полости рта должна проводиться не реже одного раза в год.
                            </li>
                        </ul>
                    </div>
